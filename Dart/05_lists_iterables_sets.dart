void main() {
  // Lista
  final numeros = [1, 2, 3, 4, 5, 5, 5];

  //Lista
  print("Ejemplos numeros: $numeros");

  //Tamaño
  print("Ejemplo tamaño: ${numeros.length}");

  //Indexar
  print("Ejemplo indexar: ${numeros[2]}");

  //Primero
  print("Ejemplo primero: ${numeros.first}");

  //Ultimo
  print("Ejemplo último: ${numeros.last}");

  //Reverso
  print("Ejemplo reverso: ${numeros.reversed}");

  //Lista
  print("Ejemplo lista: ${numeros.toList()}");

  //toSet (Conjunto)
  print("Ejemplo toSet: ${numeros.toSet()}");
}
